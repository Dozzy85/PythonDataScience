"""Operator == Operator != o <> verifano se quello che c'è a sinistra è diverso da quello a destra"""
"""Operators:
<
>
<=
>=
and
or
not
"""
result = 4 < 5 #True
print(result)

result2 = not(5 > 1) #False perchè con l'operatore not sarà il contrario
print(result2)

print(result or result2) #Se almeno uno dei due è True allora il risultato sarà True
print(result and result2) #Entrambi devono essere True per dare come risultato True